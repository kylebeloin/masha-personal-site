const sortServiceByType = (type) => {
  switch (type) {
    case "Editorial Service":
      return 10000;
    case "Manuscript Reviewer":
      return 9999;
    case "Conference Abstract Reviewer":
      return 9998;
    case "Conference Organization":
      return 9997;
    case "Grant Reviewer":
      return 9996;
    case "Mentorship & Supervision":
      return 9995;
    case "Professional Affiliations":
      return 9994;
    case "Leadership Positions":
      return 9993;
    default:
      return 9992;
  }
};

const sortServiceByActive = (active) => {
  switch (active) {
    case 1:
      return 9999;
    case 0:
      return -1;
    default:
      return Math.max(...active);
  }
};

export const sortService = (a, b) => {
  const sortedByType = sortServiceByType(b.type) - sortServiceByType(a.type);
  if (sortedByType !== 0) {
    return sortedByType;
  }
  const sortedByActive =
    sortServiceByActive(b?.active ?? 1) - sortServiceByActive(a?.active ?? 1);

  return sortedByActive;
};

export default service;
